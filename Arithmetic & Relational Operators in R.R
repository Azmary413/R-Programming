#Arithmetic Operators
x = 5
y =6

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%%y)
print(x^y)

#In vector
v1 = c(3,5,7,8)
v2 = c(2,4,6,7)
print(v1+v2)
print(v1 - v2)
print(v1*v2)
print(v1/v2)
print(v1%%v2)
print(v1^v2)

#Relational Operators
v3 = c(3,5,7,8)
v4 = c(2,6,7,9)
print(v3 > v4)
print(v3 < v4)
print(v3 != v4)
print(v3 <= v4)
print(v3 >= v4)
