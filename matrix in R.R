#matrix
mat1 = matrix(c(1:12),nrow=4, byrow = TRUE)
print(mat1)

mat2 = matrix(c(5:20),nrow=4)
print(mat2[3,3])

#matrix Summation
mat3=matrix(c(2:13),nrow=4)
